int	ft_iterative_factorial(int nb)
{
	int	f;
	int	r;

	f = 1;
	r = 1;
	if (nb < 0)
	{
		return (0);
	}
	if (nb == 0)
	{
		return (1);
	}
	while (f != nb)
	{
		r *= (f + 1);
		f++;
	}
	return (r);
}
